void main(List<String> args) {
  int a = 8;
  int b = 4;
  int c = a + b;
  int d = a - b;
  int e = a * b;
  double f = a / b;


  //penjumlahan
  print('$a + $b hasil penjumlahan = $c');

  //pengurangan
  print('$a - $b hasil pengurangan = $d');

  //perkalian
  print('$a * $b hasil perkalian = $e');

  //pembagian
  print('$a / $b hasil pembagian = $f');
}
